// บีจา 
var ico_blue = document.querySelectorAll(".ico-blue");
document.querySelector(".bg-blue").addEventListener("mouseover", function(){
    document.querySelector(".bg-blue").style.fill = "#0aaecc";
    document.querySelector(".txt-blue").style.fill = "white";
    for (let i = 0; i < ico_blue.length; i++) {
        ico_blue[i].style.fill = "white";
    }
})
document.querySelector(".bg-blue").addEventListener("mouseout", function(){
    document.querySelector(".bg-blue").style.fill = "#f9f9f9";
    document.querySelector(".txt-blue").style.fill = "#757575";
    for (let i = 0; i < ico_blue.length; i++) {
        ico_blue[i].style.fill = "#757575";
    }
})


